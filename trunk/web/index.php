<br />
<span class="PostHeader">TDStool</span> is a software
tool for the numerical solution of the Time Dependent
linear and nonlinear Schrödinger equation. 
It computes the dynamics of a single-particle quantum state in the real-space representation and deals with
the input/output through a user-friendly interface.
<br />
Key-features of TDStool include: <br />
<table class="table" width="100%">
<tbody>
<tr>
<td valign="top" width="32%">
<UL>
 -Physics-
 <LI> 1D*, 2D or 3D box domain. 
 <LI> Solution of (linear) Schrödinger equation or (nonlinear) Gross–Pitaevskii equation.
 <LI> Arbitrary electric potential landscape. 
 <LI> Time-dependent uniform electric and magnetic fields*.
 <LI> Closed or periodic boundary conditions.
 <LI> Straightforward subdomain normalization check.
</UL>
</td>
<td valign="top" width="2%">
</td>
<td valign="top" width="32%">
<UL>
 -Numerics-
 <LI> Detailed documentation.
 <LI> Two available numerical algorithms: Fourier split-step and box-integration method.
 <LI> Uniform and non-uniform (x-y-z separable) discretization grid.
 <LI> Gauge-independent discretization*.
</UL>
</td>
<td valign="top" width="2%">
</td>
<td valign="top" width="32%">
<UL>
 -Software-
 <LI> Available as Linux and Windows binaries or Fortran90 source code.
 <LI> Input setup and output visualization through a clean GUI.
 <LI> Command-line mode.
 <LI> Easy result harvesting for post-processing.
 <LI> Embedded numerical libs or link to external hi-performance libs*.
</UL>
</td>
</tr>
</tbody>
</table>
<UL>
* planned on future releases (Note that not all features are independent.)
</UL>
