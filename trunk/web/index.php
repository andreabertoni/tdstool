<br />
"The simplicity of use of the tool, that will run on a standard PC and will be freely available for download,
makes it an ideal companion for scientists that need a quick, though accurate modeling
of their time-dependent problems.
TDStool will contain few simple ready-to-use examples and an intuitive graphical representation of its output.
A specific effort will be devoted in separating the details of the numerical modeling
(accessible through advanced options and source-code handling) from the system physics."
<br />
(from the TDStool project proposal)
